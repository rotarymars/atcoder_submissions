#include <algorithm>
#include <iostream>
#include <string>
#include <vector>
using namespace std;

int main() {
  int n;
  cin >> n;
  vector<int> a(n);
  for (int i = 0; i < n; ++i) cin >> a[i];
  sort(rbegin(a), rend(a));
  vector<string> b{to_string(a[0]), to_string(a[1]), to_string(a[2])};
  vector<int> x{0, 1, 2};
  unsigned long long m = 0;
  do {
    unsigned long long v = stol(b[x[0]] + b[x[1]] + b[x[2]]);
    m = max(m, v);
  } while (next_permutation(begin(x), end(x)));
  cout << m << endl;
}
